import os
import sys
import subprocess
import secrets
from pathlib import Path

def check_python_version():
    """Check if Python version is 3.7 or higher"""
    if sys.version_info < (3, 7):
        print("âŒ Error: Python 3.7 or higher is required.")
        print(f"   Current version: {sys.version}")
        sys.exit(1)
    print(f"âœ… Python version: {sys.version.split()[0]}")

def create_directories():
    """Create necessary directories"""
    directories = ['uploads', 'instance', 'logs']
    for directory in directories:
        Path(directory).mkdir(exist_ok=True)
        print(f"âœ… Created directory: {directory}/")

def install_dependencies():
    """Install Python dependencies"""
    print("\nðŸ“¦ Installing dependencies...")
    try:
        subprocess.check_call([sys.executable, "-m", "pip", "install", "--upgrade", "pip"])
        subprocess.check_call([sys.executable, "-m", "pip", "install", "-r", "requirements.txt"])
        print("âœ… Dependencies installed successfully!")
    except subprocess.CalledProcessError as e:
        print(f"âŒ Error installing dependencies: {e}")
        print("   Try running: pip install -r requirements.txt manually")
        return False
    return True

def create_env_file():
    """Create .env file with secure defaults"""
    env_path = Path('.env')
    if env_path.exists():
        response = input("\nâš ï¸  .env file already exists. Overwrite? (y/N): ")
        if response.lower() != 'y':
            print("   Keeping existing .env file")
            return
    
    # Generate a secure secret key
    secret_key = secrets.token_hex(32)
    
    env_content = f"""# WordPress API Manager - Environment Configuration
# Generated by setup.py

# Flask Configuration
SECRET_KEY={secret_key}
FLASK_ENV=development
FLASK_DEBUG=True

# Database Configuration
DATABASE_URL=sqlite:///wordpress_manager.db

# Server Configuration
HOST=0.0.0.0
PORT=5000

# Session Configuration
SESSION_TYPE=filesystem
PERMANENT_SESSION_LIFETIME=604800  # 7 days in seconds

# Security Settings (for production)
# WTF_CSRF_ENABLED=True
# SESSION_COOKIE_SECURE=True
# SESSION_COOKIE_HTTPONLY=True
# SESSION_COOKIE_SAMESITE=Lax

# Optional: External API Keys (for future enhancements)
# OPENAI_API_KEY=your-openai-api-key
# GOOGLE_ANALYTICS_ID=your-ga-id
"""
    
    with open(env_path, 'w') as f:
        f.write(env_content)
    
    print(f"âœ… Created .env file with secure configuration")
    print(f"   Secret key generated: {secret_key[:20]}...")

def initialize_database():
    """Initialize the database"""
    print("\nðŸ—„ï¸  Initializing database...")
    try:
        from app import app, db
        with app.app_context():
            db.create_all()
            print("âœ… Database initialized successfully!")
            print("   Database location: wordpress_manager.db")
    except Exception as e:
        print(f"âŒ Error initializing database: {e}")
        print("   Try running: python init_db.py")
        return False
    return True

